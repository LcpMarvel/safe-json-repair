// Build the Rust core to wasm and inline it into the JS package.
//
// Pipeline: cargo build (wasm32, speed-tuned release, -j2 per repo memory) →
// wasm-bindgen (--target web) → wasm-opt -O3 → base64-inline the bytes into a
// generated TS module. The result is a single self-contained, dependency-free
// bundle that runs in Node / Bun / Deno / browsers / edge with a synchronous
// API (no fetch, no fs, no bundler config).
//
// We optimize for **speed**, not bytes: `opt-level = 3` (release profile) +
// `wasm-opt -O3` is ~25-32% faster on 1 MB inputs than the size-tuned `"z"` /
// `-Oz`, while costing only ~0.5% (≈286 B) more gzipped. The size knobs that
// don't hurt speed (lto, codegen-units = 1, strip) stay on.
//
// Consumers never run this — they get the prebuilt dist/. Only maintainers do.

import { execFileSync } from 'node:child_process';
import { existsSync, mkdirSync, readFileSync, writeFileSync, rmSync, copyFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { gzipSync } from 'node:zlib';

const here = dirname(fileURLToPath(import.meta.url));
const jsDir = resolve(here, '..'); // bindings/js
const repoRoot = resolve(jsDir, '..', '..'); // repo root
const cargoBin = join(process.env.HOME ?? '', '.cargo', 'bin');
const env = { ...process.env, PATH: `${cargoBin}:${process.env.PATH ?? ''}`, CARGO_BUILD_JOBS: '2' };

const WASM_TARGET = 'wasm32-unknown-unknown';

const run = (cmd, args, opts = {}) => {
  process.stdout.write(`$ ${cmd} ${args.join(' ')}\n`);
  execFileSync(cmd, args, { stdio: 'inherit', env, ...opts });
};

// Capture stdout without inheriting (for preflight probes).
const capture = (cmd, args) => execFileSync(cmd, args, { env, encoding: 'utf8' });

// --- Preflight: fail fast with an actionable message if the toolchain is not
// ready, rather than emitting a cryptic mid-pipeline error. -------------------
function preflight() {
  // 1. The wasm-opt binary ships as a local devDependency (binaryen).
  const wasmOpt = join(jsDir, 'node_modules', '.bin', 'wasm-opt');
  if (!existsSync(wasmOpt)) {
    throw new Error(
      `wasm-opt not found at ${wasmOpt}. Run \`npm install\` in bindings/js (binaryen is a devDependency).`,
    );
  }

  // 2. cargo + the wasm target must be installed.
  try {
    capture('cargo', ['--version']);
  } catch {
    throw new Error('cargo not found on PATH. Install Rust via https://rustup.rs.');
  }
  let installedTargets = '';
  try {
    installedTargets = capture('rustup', ['target', 'list', '--installed']);
  } catch {
    // No rustup (e.g. distro Rust). We can't verify; let cargo surface it.
    installedTargets = WASM_TARGET; // assume present, defer to cargo's error
  }
  if (!installedTargets.split(/\s+/).includes(WASM_TARGET)) {
    process.stdout.write(`Installing missing Rust target ${WASM_TARGET}…\n`);
    run('rustup', ['target', 'add', WASM_TARGET]);
  }

  // 3. wasm-bindgen CLI must be installed (cargo install wasm-bindgen-cli).
  try {
    capture('wasm-bindgen', ['--version']);
  } catch {
    throw new Error(
      'wasm-bindgen CLI not found. Install with `cargo install wasm-bindgen-cli` ' +
        '(keep its version in sync with the wasm-bindgen crate dependency).',
    );
  }

  return { wasmOpt };
}

const { wasmOpt } = preflight();

const genDir = join(jsDir, 'src', 'generated');
const tmpDir = join(jsDir, '.wasm-build');
rmSync(genDir, { recursive: true, force: true });
rmSync(tmpDir, { recursive: true, force: true });
mkdirSync(genDir, { recursive: true });
mkdirSync(tmpDir, { recursive: true });

// 1. Compile the wasm crate (speed-tuned release profile, limited parallelism).
run('cargo', ['build', '-p', 'safe-json-repair-wasm', '--target', WASM_TARGET, '--release', '-j', '2'], {
  cwd: repoRoot,
});

const cargoWasm = join(repoRoot, `target/${WASM_TARGET}/release/safe_json_repair_wasm.wasm`);

// 2. Generate the JS glue (--target web → ESM, browser-safe, sync initSync path).
run('wasm-bindgen', [cargoWasm, '--out-dir', tmpDir, '--target', 'web', '--omit-default-module-path']);

// 3. Optimize the wasm with wasm-opt -O3 (speed; binaryen, a local dev dep).
const bgWasm = join(tmpDir, 'safe_json_repair_wasm_bg.wasm');
const optWasm = join(tmpDir, 'opt.wasm');
run(wasmOpt, ['-O3', '--enable-bulk-memory', '--enable-nontrapping-float-to-int', bgWasm, '-o', optWasm]);

// 4. Copy the glue + types, and base64-inline the optimized wasm bytes.
copyFileSync(join(tmpDir, 'safe_json_repair_wasm.js'), join(genDir, 'glue.js'));
copyFileSync(join(tmpDir, 'safe_json_repair_wasm.d.ts'), join(genDir, 'glue.d.ts'));

const bytes = readFileSync(optWasm);
const b64 = bytes.toString('base64');
writeFileSync(
  join(genDir, 'wasm-inline.ts'),
  `// AUTO-GENERATED by scripts/build-wasm.mjs — do not edit.\n` +
    `// The Rust core compiled to wasm (${bytes.length} bytes), base64-inlined so the\n` +
    `// package is a single self-contained, universally-loadable artifact.\n` +
    `export const WASM_BASE64 = ${JSON.stringify(b64)};\n`,
);

const gzipBytes = gzipSync(bytes).length;
rmSync(tmpDir, { recursive: true, force: true });

process.stdout.write(
  `\n✔ wasm inlined: ${bytes.length} bytes raw, ${(gzipBytes / 1024).toFixed(1)}KB gzip\n`,
);
